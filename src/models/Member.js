const mongoose = require("mongoose");

module.exports = mongoose.model(
  "Member",
  new mongose.Schema({
    firstName: {
      type: String,
      required: true,
    },
    lastName: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    phoneNumber: {
      type: String,
      required: true,
    },
  })
);
